#include <iostream>

#include <cmath>

using namespace std;

int main () {
    cout <<sqrt(44)<<endl; // (square root) sqrt(577) -- This is called library function
    cout <<cbrt(1000)<<endl;  // cube root
    cout <<min(8,16)<<endl;   // Minimum
    cout <<max(4,8);          // Maximum
    cout <<pow(3,6);           // 6 is power of 3         
}